# 2) relational operator - used to compare 2 values and it will result in boolean output
# <,<=,>,>=,==,!=

num1=100
num2=200
print(num1<num2) #100>200 false
print(num1>num2) #100<200 True
print(num1==num2) #100==200 False
print(num1<=num2) # 100<=100 true
print(num1!=num2) #100!=200 True
print(num1!=0) #True
print(num2!=0) #True